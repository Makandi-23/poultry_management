import 'package:flutter/material.dart';
import 'package:poultry_management/screens/login_screen.dart';
import 'package:poultry_management/theme/theme.dart';
import 'package:poultry_management/widgets/customScaffold.dart';
import 'package:http/http.dart' as http;
import 'dart:convert';
import 'package:shared_preferences/shared_preferences.dart';

class RegisterScreen extends StatefulWidget {
  const RegisterScreen({super.key});

  @override
  State<RegisterScreen> createState() => _RegisterScreenState();
}

class _RegisterScreenState extends State<RegisterScreen> {
  final _formSignupKey = GlobalKey<FormState>();
  final TextEditingController _fullnameController = TextEditingController();
  final TextEditingController _emailController = TextEditingController();
  final TextEditingController _passwordController = TextEditingController();
  bool agreePersonalData = false;
   bool isLoading = false;

   Future<void> registerUser(String fullname, String email, String password) async {
  const String apiUrl = "https://tujengeane.co.ke/Poultry/register.php";
  final Uri uri = Uri.parse(apiUrl).replace(queryParameters: {
    "fullname": fullname,
    "email": email,
    "password": password,
  });

  setState(() {
    isLoading = true;
  });

  try {
    final response = await http.get(uri);

    // Parse server response
    final responseData = json.decode(response.body);
    if (responseData['code'] == 1) {
      // Save user data locally (optional, used for Profile later)
      SharedPreferences prefs = await SharedPreferences.getInstance();
       
          await prefs.setString('fullname', fullname);
       

       
          await prefs.setString('email', email);
       
      // Success - show message and navigate to Login Screen
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text(responseData['message'])),
      );
      Navigator.pushReplacement(
        context,
        MaterialPageRoute(builder: (context) => const LoginScreen()),
      );
    } else {
      // Error from the server
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text(responseData['message'])),
      );
    }
  } catch (error) {
    // Handle network/server errors
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(content: Text('An error occurred: $error')),
    );
  } finally {
    setState(() {
      isLoading = false;
    });
  }
}

